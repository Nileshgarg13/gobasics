package main
import "fmt"
func main(){
	var a = "normal declare"
	fmt.Println(a)
	var b int
	b=2
	fmt.Println(b)
	var c string = "declared as string"
	fmt.Println(c)
	var d,e int =5,6
	fmt.Println(d,e)
	var f bool=false
	fmt.Println(f)
	var g int
	fmt.Println(g)
	h:="declared using shorthand syntax "
	fmt.Println(h)
}